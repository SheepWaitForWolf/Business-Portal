<h3>Applying for a Temporary Road Closure or Temporary Parking Restriction</h3>


<h3>Terms of Application</h3>


<p>This procedure is for businesses, charitable organisations and residents who wish to apply for a Temporary Order for road closure / parking restrictions / or similar. This facility may be granted to enable works on or adjacent to the road to be carried out in safety or for a charitable or community event to take place on the road. Temporary orders will not be permitted in order to allow contractors the use of the road for the purposes of site compounds.</p>


<h3>Temporary Restriction durations and fees:</h3>


<p>1-5 days inclusive – Notice of Closure (£226.00 fee) Order of Closure (£396.00 fee) NB: For some charitable or community events the fee may be waived, however, applicants must comply with all other requirements.</p>


<h3>Application period and notice required</h3>


<p>All initial application forms and fees must be received at a minimum of 28 days before the required date of commencement of the restriction. In addition, all of the supporting information must be received and all details must be agreed and approved at a minimum of 21 days before the required date of commencement of the restriction.  Applicants are advised to take account of the time required to agree, organise and implement diversion schemes where these are necessary.</p>


<h3>Emergency road closures</h3>


<p>Emergency closures are only permitted where there is a present or imminent danger to persons or property. In case of emergency, please telephone 0300 123 4510 immediately.</p>


<h3>Applicant’s responsibility</h3>


<p>It is the applicant’s responsibility to arrange, install, maintain and meet the cost of all on-site signing (including the diversion route where applicable; see Diversion Routes below) in accordance with Chapter 8 of the Traffic Signs Manual and / or Traffic Signs Regulations and General Directions throughout the duration of the temporary restriction. It is the Applicant's responsibility to ensure that appropriately accredited operators only are employed to place signage on the road network. It is the Applicant's responsibility to undertake initial liaison with bus companies, and other service organisations likely to be affected, and provide the Council with evidence of this.</p>


<p>Some useful telephone numbers are listed below, however other services may need to be contacted in addition.</p>



Strathclyde Police – 101

Strathclyde Partnership for Transport (SPT) switch board – 0141 332 6811 (ask for the Operations Manager)

East Dunbartonshire Council Roads and Neighbourhood Services – 0300 123 4510 (ask for the Roads & Neighbourhood Operations Manager)

Prior warning to the public, via notices (in road stands) posted on all approaches, at least seven days prior to commencement is also required, as is a letter drop to all affected frontages at least seven days prior to commencement.



<h3>Diversion routes</h3>


<p>East Dunbartonshire Council has a network of traffic sensitive routes on which working times may be restricted, or where undue traffic disruption should be avoided. Our Neighbourhood Services Manager has a duty to limit the effect on traffic movement and may therefore impose conditions on diversions etc.</p>


<p>Diversions on the distributor road network will need to be approved by the Neighbourhood Services Manager.</p>


<h3>Supporting information required</h3>



<p> Plans at an appropriate scale illustrating the temporary restriction, diversion route and signing details (where applicable)</p>

<p> Fee (cheques made payable to East Dunbartonshire Council)</p>

<p> A copy of the applicants Public Liability Insurance with a minimum of £5,000,000 cover</p>

<p> NB: Applicants should also be aware that they might require other licences from the Council, e.g. road opening, materials on the highway, craning, hoardings etc.</p>


