<h3> Licence summary</h3>


<p> If you wish to temporarily close a road in East Dunbartonshire you must have consent from East Dunbartonshire Council. This temporary road closure procedure is for businesses, residents, charitable and other organisations, who may wish to apply for a temporary road closure or temporary parking restriction. </p>


<h3> Fee </h3>


<p> If you plan to close a road for less than five days the fee is £226.00 for a temporary closure by notice. At least one weeks notice is required for the paperwork to be prepared.</p>


<p>If you plan to close the road for more than five days the fee is £396.00 for a temporary closure by order. At least four weeks notice is required for the paperwork to be prepared through Legal Services.</p>

<p>Please note if you require a temporary road closure for example for a proposed event then the fee will be £396.00. At least four weeks notice is required for the paperwork to be prepared through Legal Services.</p>


