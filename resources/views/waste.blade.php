<h3>Compost your garden waste</h3>

Full information on home composting can be found at Zero Waste Scotland Home Composting (link is external).  You can buy composters at garden centres, supermarkets and online. 

<h3>Collection of Household Waste</h3>

To make sure your bin is emptied, place it at the kerbside or collection point by 7am.  To find your collection day, please view the document section.

<h3>Report a Missed Bin</h3>

<p>Before reporting a missed bin please ensure that:</p>
<ul>
<li>your bin was on the kerbside from 7am on your scheduled collection day</li>
<li>you have put your bin out on the right day - check your collection day here</li>
<li>the lid is fully closed</li>
<li>if it is a recycling bin, that the correct items are in it</li>
<li>access to the bin is not blocked by roadworks or parked cars.</li>
<li>if missed please fill in the missed bin form.</li>
</ul>

