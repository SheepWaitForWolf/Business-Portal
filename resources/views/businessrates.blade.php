		 <p>Business Rates (or non domestic rates) are a tax on occupation of non domestic subjects, levied by the Council on behalf of the Scottish Government.

How your business rates are calculated

Your annual Rates bill is calculated by multiplying the rateable value of your property by the annual rate poundage. The Assessor for Dunbartonshire and Argyll & Bute determines the rateable value, as shown in the Valuation Roll, and the Scottish Government sets the poundage each year.

The rate poundage for 2016-17 is 48p per £; a supplement of 2.6p per £ applies where the rateable value of the property is greater than £35,000.

You can check the rateable value of any non domestic subject in Scotland at any time by visiting the website of the Scottish Assessors Association (link is external). A paper copy of the Valuation Roll can be inspected, free of charge, at William Patrick Library, 2-4 West High Street, Kirkintilloch (opening hours 10am-8pm Monday to Thursday, 10am-5pm Friday to Saturday). </p>
