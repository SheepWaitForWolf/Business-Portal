<h3>Failed application redress</h3>

<p>Please contact the Council in the first instance.</p>

<p>Within 28 days of the date of the decision to refuse their application, the applicant may require the licence authority to give the reasons for their decision.  The applicant may appeal to the sheriff against the decision, within 28 days of the decision, as long as they have already followed any available procedure in terms of stating their case to the Council.</p>

The appeal will only be successful if the sheriff considers that the Council, in making their decision, had:
<ul>
<li>erred in law</li>
<li>based their decision on an incorrect material fact</li>
<li>acted contrary to natural justice</li>
<li>exercised their discretion in an unreasonable manner</li>
<li>The applicant may then appeal on a point of law from the Sheriff's decision to the Court of Session within 28 days from the date of the Sheriff’s decision</li>
</ul>

<h3>Licence holder redress</h3>

<p>If the Council decides to suspend a licence, not to renew a licence, or not to consent to material changes to the premises or vehicle used in the course of a licence holder's work, the licence holder may appeal against the decision within 28 days of being notified, as long as they have already followed any available procedure in terms of stating their case to the local authority. </p>

<h3>Other redress</h3>

<p>Objections or representations relating to a licence application may be made in writing to the local authority, within 21 days of notice of the licence application being given, stating:</p>

the grounds of the objection or nature of the representation
the name and address of the person making the representation

A Chief Constable, or anyone who has made a relevant objection or representation regarding the licence, may appeal against a decision within 28 days of being notified, as long as they have already followed any available procedure in terms of stating their case to the Council.
