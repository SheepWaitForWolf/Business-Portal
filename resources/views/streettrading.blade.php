		 <p>The Council is responsible for the processing of all licence applications including Street Trading (hawking, selling or exposing for sale any article or offering to carry out a service (fee/money's worth) in a public place from a vehicle or kiosk/moveable stall.

<h3> Licence summary </h3>

<p> To operate as a street trader you require a licence from the Council. You must notify the licensing authority of any material change in your circumstances.</p>

Eligibility criteria

You must not be disqualifies from holding a licence and you must be fit to be the holder of the licence
The vehicle you use in order to carry out the work must also be suitable for that purpose
You must not have applied for the same licence within the last year, unless there has been a material change in your circumstances since your last application

A street trading licence is not required for:
<ul>
<li>the sale of newspapers only</li>
<li>the sale of milk by or on behalf of a person registered under the Food Safety Act 1990</li>
<li>the sale of coal or fuel derived from coal or coke</li>
<li>any activity for which a certificate under the Pedlar's Act 1871 has been granted</li>
<li>any activity for which a more specific licence is required</li>
<li>organising or participating in a public charitable collection, which has been granted permission under subsection (16) of section 119 of the Civic Government (Scotland) Act 1982</li>
<li>A summary of the regulation (link is external) relating to this licence is available to view or download.</li>
</ul>

<h3>Fees</h3>

£243.00 for a Static Street Trader Licence (3 years), ie one which is sited on a permanent location - this fee includes advertising costs
£147.00 for a Mobile Street Trader Licence (3 years) which does not need advertising.
<br>
Applications must:
<ul>
<li>be in writing (including by electronic means)</li>
<li>be signed by the applicant (including by electronic means)</li>
<li>contain the applicant's name and address, and those of any employee who will manage the activity on a day-to-day basis, and the area in which the activity is to be carried out
a Static Street Trader is required to display a site notice for a 21 day period and thereafter must supply the licensing authority with a certificate confirming that he had complied with the requirement.<li>
</ul>
<br>
The Council will then:
<ul>
<li>send a copy of the application to the Chief Constable and relevant consultees</li>
<li>place a Notice in at least one local or national newspaper which states the premises and applicant details contained in the application, that any objections or representations may be made to the local authority, and how such representations should be made</li>
<li>keep a register of applications in which they will enter the details of the receipt of the application, their final decision and reasons for the decision, the details of the terms of each licence granted and a note of any suspension, variation of the terms, or surrender, of a licence</li>
<li>The register must be available for inspection by any member of the public at reasonable times and places and any member of the public must be permitted to make a copy of it.  Where the Council fails to make a decision within six months of the application, the licence is held to be granted or renewed for a default period of one year.</li>
</ul>

If your application is made in respect of an activity which includes the sale of food, and involves the use of a vehicle, kiosk or moveable stall, you must produce a certificate by the food authority, stating that the premises comply with the requirements of any relevant regulations under section 16 of the Food Safety Act 1990.

<h3>Will Tacit Consent apply?</h3>

<p>Yes.  This means that you will be able to act as though your application is granted if you have not heard from the Council by the end of the 130 day period permitted for final determination of the application.</p>

